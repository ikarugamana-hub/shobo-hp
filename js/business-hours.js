var HOLIDAYS = ['2026-01-01','2026-01-12','2026-02-11','2026-02-23','2026-03-20','2026-04-29','2026-05-03','2026-05-04','2026-05-05','2026-05-06','2026-07-20','2026-08-11','2026-09-21','2026-09-22','2026-09-23','2026-10-12','2026-11-03','2026-11-23','2027-01-01','2027-01-11','2027-02-11','2027-02-23','2027-03-21','2027-03-22','2027-04-29','2027-05-03','2027-05-04','2027-05-05','2027-07-19','2027-08-11','2027-09-20','2027-09-23','2027-10-11','2027-11-03','2027-11-23','2028-01-01','2028-01-10','2028-02-11','2028-02-23','2028-03-20','2028-04-29','2028-05-03','2028-05-04','2028-05-05','2028-07-17','2028-08-11','2028-09-18','2028-09-22','2028-10-09','2028-11-03','2028-11-23'];
function isOpen(){
  var d = new Date(new Date().toLocaleString('en-US', {timeZone: 'Asia/Tokyo'}));
  var day = d.getDay();
  if (day === 0 || day === 6) return false;
  var mm = ('0' + (d.getMonth() + 1)).slice(-2);
  var dd = ('0' + d.getDate()).slice(-2);
  var ymd = d.getFullYear() + '-' + mm + '-' + dd;
  if (HOLIDAYS.indexOf(ymd) !== -1) return false;
  var md = mm + '-' + dd;
  if (md >= '08-13' && md <= '08-15') return false;   // お盆
  if (md >= '12-29' || md <= '01-03') return false;   // 年末年始
  var h = d.getHours();
  return h >= 10 && h < 17;
}
