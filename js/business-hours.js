function isOpen(){
  var d = new Date(new Date().toLocaleString('en-US', {timeZone: 'Asia/Tokyo'}));
  var day = d.getDay();
  if (day === 0 || day === 6) return false;
  var mm = ('0' + (d.getMonth() + 1)).slice(-2);
  var dd = ('0' + d.getDate()).slice(-2);
  var ymd = d.getFullYear() + '-' + mm + '-' + dd;
  if (HOLIDAYS.indexOf(ymd) !== -1) return false;
  var md = mm + '-' + dd;
  if (md >= '08-13' && md <= '08-15') return false;   // お盆
  if (md >= '12-29' || md <= '01-03') return false;   // 年末年始
  var h = d.getHours();
  return h >= 10 && h < 17;
}
